	//-------------------------------------------------------------------
	//「更新」１フレーム毎に行う処理
	void  Object::UpDate()
	{
		
		this->pos += this->moveVec;
		//画面外に出たら
		if (this->CheckScreenFullOut()) { this->Kill(); }

		//当たり判定
		{
			ML::Box2D me = this->OffsetBox();

			if (ge->qa_Player == nullptr) { return; }

			if (ge->qa_Player->CheckHit(me)) {
				AttackInfo at = { 1 };
				ge->qa_Player->Received(this, at);
				this->Kill();
			}
		}

	}
	//-------------------------------------------------------------------
	//「２Ｄ描画」１フレーム毎に行う処理
	void  Object::Render2D_AF()
	{
		ML::Box2D draw = this->OffsetBox();
		//ML::Box2D src(192, 16, 16, 16);
		ML::Box2D src(0, 136, 24, 24);
		this->res->img->Rotation(this->angle, ML::Vec2(draw.w / 2.0f, draw.h / 2.0f));
		this->res->img->Draw(draw, src);
		this->res->img->Rotation(0.0f, ML::Vec2(draw.w / 2.0f, draw.h / 2.0f));
	}

